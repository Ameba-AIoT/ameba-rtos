/*------------------------------------------------------------------------------

    Function name: SetDecRegister

        Functional description:

        Inputs:

        Outputs:

        Returns:

------------------------------------------------------------------------------*/
void SetDecRegister(u32 *regBase, u32 id, u32 value)
{
	u32 tmp;

	ASSERT(id < HWIF_LAST_REG);

	tmp = regBase[hwDecRegSpec[id].reg_id];
	tmp &= ~(regMask[hwDecRegSpec[id].bit_num] << hwDecRegSpec[id].bit_off);
	tmp |= (value & regMask[hwDecRegSpec[id].bit_num]) << hwDecRegSpec[id].bit_off;
	regBase[hwDecRegSpec[id].reg_id] = tmp;
}

void SetPpRegister(u32 *ppRegBase, u32 id, u32 value)
{
	u32 tmp;

	ASSERT(id < HWIF_LAST_REG);

	/* hw reg spec is based on decoder so we have to adjust the register offset
	 * because we are getting PP base register as parameter
	 */

	tmp = ppRegBase[hwDecRegSpec[id].reg_id - PP_REG_START];
	tmp &= ~(regMask[hwDecRegSpec[id].bit_num] << hwDecRegSpec[id].bit_off);
	tmp |= (value & regMask[hwDecRegSpec[id].bit_num]) << hwDecRegSpec[id].bit_off;
	ppRegBase[hwDecRegSpec[id].reg_id - PP_REG_START] = tmp;
}
/*------------------------------------------------------------------------------

    Function name: GetDecRegister

        Functional description:

        Inputs:

        Outputs:

        Returns:

------------------------------------------------------------------------------*/
u32 GetDecRegister(const u32 *regBase, u32 id)
{
	u32 tmp;

	ASSERT(id < HWIF_LAST_REG);

	tmp = regBase[hwDecRegSpec[id].reg_id];
	tmp = tmp >> hwDecRegSpec[id].bit_off;
	tmp &= regMask[hwDecRegSpec[id].bit_num];
	return (tmp);
}

u32 GetPpRegister(const u32 *ppRegBase, u32 id)
{
	u32 tmp;

	ASSERT(id < HWIF_LAST_REG);

	/* hw reg spec is based on decoder so we have to adjust the register offset
	 * because we are getting PP base register as parameter
	 */

	tmp = ppRegBase[hwDecRegSpec[id].reg_id - PP_REG_START];
	tmp = tmp >> hwDecRegSpec[id].bit_off;
	tmp &= regMask[hwDecRegSpec[id].bit_num];
	return (tmp);
}
