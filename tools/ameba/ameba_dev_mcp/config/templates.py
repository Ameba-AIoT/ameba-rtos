EMPTY_BOARD_INFO = _read_template(BOARD_INFO_TEMPLATE_PATH)


def render_board_info(model: BoardInfo) -> str:
    """Render BoardInfo back to json5. Passwords are NOT masked here —
    this writes back the raw structure to disk; masking happens only for
    MCP resource exposure."""
    header = (
        "/*\n"
        " * board_info.json5 — test-bench config.\n"
        " * See tools/ameba/ameba_dev_mcp/docs/board_info.md for full schema.\n"
        " */\n"
    )
    body: List[str] = []
    body.append("{")
    body.append(f'  "schema_version": {model.schema_version},')
    body.append("")
    body.append('  "defaults": {')
    d = model.defaults
    body.append(f'    "baudrate": {d.baudrate},')
    body.append(f'    "monitor_baudrate": {d.monitor_baudrate},')
    body.append(f'    "chip_erase": {json.dumps(d.chip_erase)}')
    body.append("  },")
    body.append("")
    if model.default_alias is not None:
        body.append(
            "  /* When tools are called without `alias` and 2+ boards are "
            "configured, this one is picked. */"
        )
        body.append(f'  "default_alias": {json.dumps(model.default_alias)},')
        body.append("")
    if not model.boards:
        body.append('  "boards": {}')
    else:
        body.append('  "boards": {')
        items = list(model.boards.items())
        for i, (alias, entry) in enumerate(items):
            sub = "    "
            sub2 = "      "
            kvs: List[str] = []
            kvs.append(f'"soc": {json.dumps(entry.soc)}')
            kvs.append(f'"transport": {json.dumps(entry.transport)}')
            kvs.append(f'"port": {json.dumps(entry.port)}')
            if entry.baudrate is not None:
                kvs.append(f'"baudrate": {entry.baudrate}')
            if entry.monitor_baudrate is not None:
                kvs.append(f'"monitor_baudrate": {entry.monitor_baudrate}')
            if entry.chip_erase is not None:
                kvs.append(f'"chip_erase": {json.dumps(entry.chip_erase)}')
            if entry.add_crlf is not None:
                kvs.append(f'"add_crlf": {json.dumps(entry.add_crlf)}')
            if entry.remote is not None:
                rem = entry.remote
                rline = f'"host": {json.dumps(rem.host)}, "port": {rem.port}'
                if rem.password is not None:
                    rline += f', "password": {json.dumps(rem.password)}'
                kvs.append(f'"remote": {{ {rline} }}')
            if entry.serial_log_record is not None:
                rec = entry.serial_log_record
                rparts = [f'"enable": {json.dumps(rec.enable)}']
                if rec.log_dir is not None:
                    rparts.append(f'"log_dir": {json.dumps(rec.log_dir)}')
                if rec.file_name is not None:
                    rparts.append(f'"file_name": {json.dumps(rec.file_name)}')
                kvs.append(f'"serial_log_record": {{ {", ".join(rparts)} }}')
            body.append(f'{sub}{json.dumps(alias)}: {{')
            for j, kv in enumerate(kvs):
                tail = "," if j + 1 < len(kvs) else ""
                body.append(f"{sub2}{kv}{tail}")
            close = "}," if i + 1 < len(items) else "}"
            body.append(f"{sub}{close}")
        body.append("  }")
    body.append("}")
    body.append("")
    return header + "\n".join(body)
